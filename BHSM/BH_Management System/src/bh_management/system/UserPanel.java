public void loadDataMessage() {
    try {
        AdminForm adminform = new AdminForm();
        JTextField adminSendField = adminform.adminSendField;
        
        pst = con.prepareStatement("SELECT * FROM `message`");
        rs = pst.executeQuery();
        
        DefaultListModel<String> listModel = new DefaultListModel<>();
        
        while(rs.next()) {
            String message = rs.getString("message");
            Date date = rs.getDate("date");
            
            String formattedMessage = message + "  _ " + date.toString();
            listModel.addElement(formattedMessage);
            
        }
        
        userReceive.setModel(listModel);
    } catch (SQLException ex) {
        Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
    }
}


    
    


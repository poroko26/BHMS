    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
       try {
    int selectedRow = userReceive.getSelectedIndex();
    if (selectedRow != -1) {
        DefaultListModel<String> listModel = (DefaultListModel<String>) userReceive.getModel();
        String uid = listModel.getElementAt(selectedRow).toString();
        
        pst = con.prepareStatement("DELETE FROM `message` WHERE id = ?");
        pst.setString(1, uid);
        int rowsDeleted = pst.executeUpdate();
        
        if (rowsDeleted >= 0) {
            JOptionPane.showMessageDialog(null, "Item deleted successfully.");
            
            // Remove the item from the list model
            listModel.remove(selectedRow);
        } else {
            JOptionPane.showMessageDialog(null, "Item not found.");
        }
    } else {
        JOptionPane.showMessageDialog(null, "No item selected.");
    }
} catch (SQLException ex) {
    Logger.getLogger(UserPanel.class.getName()).log(Level.SEVERE, null, ex);
}



    }//GEN-LAST:event_deleteBtnActionPerformed

